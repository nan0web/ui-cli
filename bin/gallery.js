#!/usr/bin/env node

import { readdirSync, readFileSync, writeFileSync, statSync, mkdirSync } from 'node:fs'
import { join, resolve, relative, dirname, basename } from 'node:path'
import process from 'node:process'

const args = process.argv.slice(2)
const snapDirArg = args.find((a) => a.startsWith('--dir='))?.split('=')[1] || '__snapshots__'
const outPathArg = args.find((a) => a.startsWith('--out='))?.split('=')[1] || 'CLI_GALLERY.md'
const matchArg = args.find((a) => a.startsWith('--match='))?.split('=')[1] || ''
const skipArg = args.find((a) => a.startsWith('--skip='))?.split('=')[1] || ''

const snapDir = resolve(process.cwd(), snapDirArg)
const outPath = resolve(process.cwd(), outPathArg)

try {
	let files = []
	try {
		const getFiles = (dir) => {
			let results = []
			const list = readdirSync(dir, { withFileTypes: true })
			for (const file of list) {
				const fullPath = join(dir, file.name)
				if (file.isDirectory()) {
					results = results.concat(getFiles(fullPath))
				} else if (file.name !== 'index.md') {
					results.push(relative(snapDir, fullPath))
				}
			}
			return results
		}

		files = getFiles(snapDir).filter(
			(f) =>
				f.endsWith('.txt') ||
				f.endsWith('.snap') ||
				f.endsWith('.md') ||
				f.endsWith('.json') ||
				f.endsWith('.jsonl') ||
				f.endsWith('.yaml') ||
				f.endsWith('.yml') ||
				f.endsWith('.log') ||
				f.endsWith('.webp') ||
				f.endsWith('.png') ||
				f.endsWith('.jpg')
		)

		if (matchArg) files = files.filter((f) => f.includes(matchArg))
		if (skipArg) files = files.filter((f) => !f.includes(skipArg))
	} catch (e) {
		if (e.code === 'ENOENT') {
			console.error(`❌ Directory not found: ${snapDir}`)
			process.exit(1)
		}
		throw e
	}

	if (files.length === 0) {
		console.error(`❌ No snapshots found in: ${snapDir}`)
		writeFileSync(outPath, '# Галерея порожня\n\nНе знайдено сніпшотів за цим фільтром.', 'utf8')
		process.exit(0)
	}

	// Sort files by name (ascending)
	files.sort((a, b) => a.localeCompare(b, undefined, { numeric: true, sensitivity: 'base' }))

	const filesByLocale = { uk: [], en: [], other: [] }
	for (const file of files) {
		if (file.includes('.uk.') || file.includes('/uk/')) {
			filesByLocale.uk.push(file)
		} else if (file.includes('.en.') || file.includes('/en/')) {
			filesByLocale.en.push(file)
		} else {
			filesByLocale.other.push(file)
		}
	}

	const outDir = dirname(outPath)
	const outFilename = basename(outPath)
	const outName = outFilename.replace(/\.md$/, '')

	const getGroupName = (file) => {
		if (file.includes('sandbox/')) {
			const m = file.match(/sandbox\/([^\/]+)\//)
			return m ? m[1] : 'misc_sandbox'
		} else if (file.includes('play/')) {
			const m = file.match(/play\/([^\/]+)\//)
			return m ? m[1] : 'misc_play'
		} else {
			const parts = file.split(/[\\/]/)
			if (parts.length > 1) return parts[0]
			return 'misc'
		}
	}

	const generateComponentContent = (groupName, locFiles, localeName, localeId, targetDir) => {
		let txtTitle = localeId === 'en' ? 'Component' : 'Компонент'
		let txtBack = localeId === 'en' ? '⬅ Back to component list' : '⬅ Назад до списку компонентів'
		let txtDesc = localeId === 'en' ? 'This file is automatically generated from reference snapshots.' : 'Цей файл генерується автоматично з еталонних зліпків (snapshots).'
		let txtComment = localeId === 'en' ? '**How to comment:** Leave comments below a relevant screen to have them integrated.' : '**Як коментувати:** Пишіть коментарі під відповідним екраном, і їх буде враховано.'
		
		let md = `# ${txtTitle}: ${groupName} (${localeName})\n\n`
		md += `[${txtBack}](./index.md)\n\n`
		md += `${txtDesc}\n`
		md += `${txtComment}\n\n`
		md += `---\n\n`

		for (const file of locFiles) {
			const isImage = file.endsWith('.webp') || file.endsWith('.png') || file.endsWith('.jpg')
			const cleanName = file.replace(/\.(txt|snap|md|json|jsonl|yaml|yml|log|webp|png|jpg)$/, '')
			const parts = cleanName.split(/[_\/\.\-]/).filter(Boolean)

			let stepNumber = ''
			if (!isNaN(parts[parts.length - 1])) {
				stepNumber = parts.pop()
			} else if (!isNaN(parts[0])) {
				stepNumber = parts.shift()
			}

			const uniqueParts = [...new Set(parts)]
			const title = uniqueParts.map((p) => p.charAt(0).toUpperCase() + p.slice(1)).join(' ')
			const finalTitle = stepNumber ? `${title} (Крок ${stepNumber})` : title

			let txtScreen = localeId === 'en' ? 'Screen' : 'Екран'
			md += `## 🖥 ${txtScreen}: \`${finalTitle}\` (\`${file}\`)\n\n`

			let runCmd = ''
			let langArg = ''
			if (file.includes('.uk.') || file.includes('/uk/')) langArg = ' --lang=uk'
			else if (file.includes('.en.') || file.includes('/en/')) langArg = ' --lang=en'

			if (file.includes('sandbox/')) {
				const compMatch = file.match(/sandbox\/([^\/]+)\//)
				if (compMatch) {
					const compName = compMatch[1].charAt(0).toUpperCase() + compMatch[1].slice(1)
					runCmd = `node play/sandbox.js --comp=${compName}${langArg}`
				} else {
					runCmd = `node play/sandbox.js${langArg}`
				}
			} else if (file.includes('play/')) {
				const demoMatch = file.match(/play\/([^\/]+)\//)
				if (demoMatch) {
					runCmd = `node play/main.js --demo=${demoMatch[1]}${langArg}`
				} else {
					runCmd = `node play/main.js${langArg}`
				}
			} else {
				runCmd = `npm run test:play`
			}

			if (runCmd) {
				let txtRun = localeId === 'en' ? 'How to run' : 'Як запустити'
				md += `> 🚀 **${txtRun}:** \`${runCmd}\`\n\n`
			}

			if (isImage) {
				const relPath = relative(targetDir, join(snapDir, file))
				md += `![${finalTitle}](${relPath})\n\n`
			} else {
				const content = readFileSync(join(snapDir, file), 'utf8')
				let syntax = 'text'
				if (file.endsWith('.html') || content.trim().startsWith('<')) syntax = 'html'
				else if (file.endsWith('.json') || file.endsWith('.jsonl')) syntax = 'json'
				else if (file.endsWith('.yaml') || file.endsWith('.yml')) syntax = 'yaml'
				else if (file.endsWith('.log')) syntax = 'ansi'
				else if (content.includes('>>') || content.includes('{')) syntax = 'javascript'
				else if (file.endsWith('.md')) syntax = 'markdown'

				md += `\`\`\`${syntax}\n${content.trim()}\n\`\`\`\n\n`
			}

			let txtBlock = localeId === 'en' ? '**💬 Comments on**' : '**💬 Коментарі щодо'
			let txtBlockSub = localeId === 'en' ? '*(write your feedback here: colors, margins, text, logic)*' : '*(напишіть тут що змінити: кольори, відступи, тексти, логіку)*'
			md += `> ${txtBlock} ${finalTitle}:\n> ${txtBlockSub}\n\n`
			md += `---\n\n`
		}
		return md
	}

	let mainMd = `# Головна Галерея Інтерфейсів (Snapshots)\n\n`
	mainMd += `Виберіть мовну версію для перегляду екранів:\n\n`

	const locales = [
		{ id: 'uk', name: '🇺🇦 Українська мова', suffix: 'uk' },
		{ id: 'en', name: '🇬🇧 Англійська мова', suffix: 'en' },
		{ id: 'other', name: '📦 Інші екрани', suffix: 'other' }
	]

	const now = Date.now()
	const oneDay = 24 * 60 * 60 * 1000
	const oneWeek = 7 * oneDay

	const COMP_EMOJIS = {
		alert: '🔔', badge: '📛', toast: '🍞', table: '📊', tabs: '📑',
		breadcrumbs: '🥖', steps: '🪜', input: '⌨️', password: '🔑',
		toggle: '🎚', confirm: '✅', select: '🔽', multiselect: '☑️',
		slider: '🎛', datetime: '📅', tree: '🌲', spinner: '⏳',
		progressbar: '🟩', basic: '📝', form: '📋', nav: '🧭',
		view: '👁', sortable: '↕️', 'ui-message': '💬'
	}
	const getEmoji = (name) => COMP_EMOJIS[name.toLowerCase()] || '📦'

	for (const loc of locales) {
		const locFiles = filesByLocale[loc.id]
		if (locFiles.length === 0) continue

		const groups = {}
		for (const file of locFiles) {
			const groupName = getGroupName(file)
			if (!groups[groupName]) groups[groupName] = { files: [], mtimeMs: 0 }
			groups[groupName].files.push(file)

			const fullPath = join(snapDir, file)
			const stats = statSync(fullPath)
			if (stats.mtimeMs > groups[groupName].mtimeMs) {
				groups[groupName].mtimeMs = stats.mtimeMs
			}
		}

		const sortedGroups = Object.entries(groups).map(([name, data]) => {
			let category = 'all'
			const diff = now - data.mtimeMs
			if (diff <= oneDay) category = 'today'
			else if (diff <= oneWeek) category = 'week'

			return { name, files: data.files, mtimeMs: data.mtimeMs, category }
		}).sort((a, b) => b.mtimeMs - a.mtimeMs)

		let locSuffixFolder = loc.suffix === 'other' ? 'other' : loc.suffix
		const locGalleryDir = join(outDir, 'docs', locSuffixFolder, 'gallery')
		mkdirSync(locGalleryDir, { recursive: true })

		let txtTitle = loc.id === 'en' ? 'Interface Gallery' : 'Галерея Інтерфейсів'
		let txtBack = loc.id === 'en' ? '⬅ Back to main gallery' : '⬅ Назад до головної галереї'
		let txtToday = loc.id === 'en' ? 'Updated Today' : 'Оновлено сьогодні'
		let txtWeek = loc.id === 'en' ? 'Updated This Week' : 'Оновлено за останній тиждень'
		let txtAll = loc.id === 'en' ? 'All Components' : 'Всі компоненти'
		let txtNone = loc.id === 'en' ? 'None' : 'Відсутні'

		let indexMd = `# ${txtTitle}: ${loc.name}\n\n`
		const relativeToRoot = relative(locGalleryDir, outDir) || '.'
		indexMd += `[${txtBack}](${relativeToRoot}/${outFilename})\n\n`

		const todayGroups = sortedGroups.filter((g) => g.category === 'today')
		const weekGroups = sortedGroups.filter((g) => g.category === 'today' || g.category === 'week')

		const formatGroup = (g) => {
			const display = g.name.charAt(0).toUpperCase() + g.name.slice(1)
			return `- [${getEmoji(g.name)} **${display}**](comp_${g.name}.md)`
		}

		indexMd += `## ${txtToday}\n`
		if (todayGroups.length === 0) indexMd += `${txtNone}\n\n`
		else indexMd += todayGroups.map(formatGroup).join('\n') + '\n\n'

		indexMd += `## ${txtWeek}\n`
		if (weekGroups.length === 0) indexMd += `${txtNone}\n\n`
		else indexMd += weekGroups.map(formatGroup).join('\n') + '\n\n'

		indexMd += `## ${txtAll}\n`
		indexMd += sortedGroups.map(formatGroup).join('\n') + '\n\n'

		writeFileSync(join(locGalleryDir, 'index.md'), indexMd, 'utf8')

		for (const g of sortedGroups) {
			const compPath = join(locGalleryDir, `comp_${g.name}.md`)
			const content = generateComponentContent(g.name, g.files, loc.name, loc.id, locGalleryDir)
			writeFileSync(compPath, content, 'utf8')
		}

		// Clean up old single generated files if they exist
		try {
			const oldSingleFile = join(outDir, 'docs', 'gallery', `${outFilename.replace(/\.md$/, '')}.${loc.suffix}.md`)
			import('node:fs').then(fs => fs.rmSync(oldSingleFile, { force: true }))
		} catch (e) {}

		const relIndex = relative(outDir, join(locGalleryDir, 'index.md'))
		mainMd += `- [**${loc.name}** (${locFiles.length} екранів)](./${relIndex})\n`
	}

	writeFileSync(outPath, mainMd, 'utf8')
	console.log(`✅ Galleries generated successfully. Main index at: ${outPathArg}`)
} catch (err) {
	console.error(`❌ Failed to generate gallery: ${err.message}`)
	process.exit(1)
}
